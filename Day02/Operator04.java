package Day02;

public class Operator04 {
	public static void main(String[] args) {
		
		//대입연산자
		int a = 6;
		int b = 5;
		
		 a += 3; //a = a + 3
		 b += 3; //b = b + 3
		 
		 System.out.println(a);
		 System.out.println(b);
		 
		 a -= 4; //a = a - 4
		 a *= 6; //a = a * 6
		 a /= 3; //a = a / 3
		 a %= 2; //a = a % 2
		 
		 System.out.println(a);
		 
	}
}
